class NumberConversion{
    public static void main(String[] args){
        String number = "seven";
        
        switch(number){
            case ("one"):
                System.out.println(1L);
                break;
            case ("two"):
                System.out.println(2L);
                break;
            case ("three"):
                System.out.println(3L);
                break;
            case ("four"):
                System.out.println(4L);
                break;
            case ("five"):
                System.out.println(5L);
                break;
            case ("six"):
                System.out.println(6L);
                break;
            case ("seven"):
                System.out.println(7L);
                break;
            case ("eight"):
                System.out.println(8L);
                break;
            case ("nine"):
                System.out.println(9L);
                break;
            case ("ten"):
                System.out.println(10L);
                break;
            default:
                System.out.println("Please enter a number from one to ten");
        }
               
    }
}